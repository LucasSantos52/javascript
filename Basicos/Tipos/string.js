/*
    * Cadeia de Caracteres

    '' aspas simples
    "" aspas duplas
    `` template literals ou template string

*/

console.log('Lucas');

/*
    caso o texto precise usar aspas, podemos usar
    aspas simples ou duplas para abraçar o texto:
*/

console.log('Lucas disse "volto logo" e saiu')
console.log("Lucas disse 'volto logo' e saiu")

/*
    Crase ou template string/literals permite a utilização 
    dos dois tipos de aspas e a adição de expressões, metodo
    chamao de interpolação:
*/

console.log(`Lucas disse "volto" 'logo' e saiu`);
console.log(`resultado: ${1 + 1}`);